package xgz.mybatis_props.model;

public class Prop {
    private String propId;

    private String propRole;

    private String propName;

    private String propLevel;

    public String getPropId() {
        return propId;
    }

    public void setPropId(String propId) {
        this.propId = propId;
    }

    public String getPropRole() {
        return propRole;
    }

    public void setPropRole(String propRole) {
        this.propRole = propRole;
    }

    public String getPropName() {
        return propName;
    }

    public void setPropName(String propName) {
        this.propName = propName;
    }

    public String getPropLevel() {
        return propLevel;
    }

    public void setPropLevel(String propLevel) {
        this.propLevel = propLevel;
    }
}